    private void btnCalcFareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcFareActionPerformed
        float tickAmt = Integer.parseInt(amtTickets_2.getText());
        float tickPrice = Integer.parseInt(fare_2.getText());
        
        if (ticketClass_2.getText() == "Business"){
            float businessFee = (float) 0.3;
            float S = (float) (tickPrice * tickAmt);
            
            float subtotal = S + (S * businessFee);
                tfSubtotal.setText(String.valueOf(subtotal));
        
            float taxFee = (float) (subtotal * 0.09);
                tfTaxes.setText(String.valueOf(taxFee));
        
            float totalCost = subtotal + taxFee;
                tfTotalCost.setText(String.valueOf(totalCost));
        }
        
        else {
            if (ticketClass_2.getText() == "First Class") {
                float fstClassFee = (float) 0.5;
                float V = (float) (tickPrice * tickAmt);

                float subtotal = V + (V * fstClassFee);
                    tfSubtotal.setText(String.valueOf(subtotal));

                float taxFee = (float) (subtotal * 0.09);
                    tfTaxes.setText(String.valueOf(taxFee));

                float totalCost = subtotal + taxFee;
                    tfTotalCost.setText(String.valueOf(totalCost));
            }

            else {
                float subtotal = tickPrice * tickAmt;
                    tfSubtotal.setText(String.valueOf(subtotal));

                float taxFee = (float) (subtotal * 0.09);
                    tfTaxes.setText(String.valueOf(taxFee));

                float totalCost = subtotal + taxFee;
                    tfTotalCost.setText(String.valueOf(totalCost));
            }
        }
        
        btnCalcFare.setEnabled(false);
        holder_name.setEnabled(true);
        holder_email.setEnabled(true);
        holder_address.setEnabled(true);
        holder_card.setEnabled(true);
        card_CSC.setEnabled(true);
        cbExpMonth.setEnabled(true);
        cbExpYear.setEnabled(true);        
    }//GEN-LAST:event_btnCalcFareActionPerformed

    
